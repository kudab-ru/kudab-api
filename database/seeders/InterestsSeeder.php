    public function run(): void
    {
        // 1) upsert узлов по slug (без parent — проставим вторым проходом)
        $slugToId = [];
        foreach (self::TAXONOMY as [$slug, $name, , $aliases]) {
            $i = Interest::updateOrCreate(['slug' => $slug], ['name' => $name]);
            $slugToId[$slug] = $i->id;
        }

        // 2) parent_id
        foreach (self::TAXONOMY as [$slug, , $parentSlug]) {
            $parentId = $parentSlug ? ($slugToId[$parentSlug] ?? null) : null;
            Interest::where('id', $slugToId[$slug])->update(['parent_id' => $parentId]);
        }

        // 3) алиасы: пересборка только для НАШИХ узлов (идемпотентно)
        $ourIds = array_values($slugToId);
        DB::table('interest_aliases')->whereIn('interest_id', $ourIds)->delete();

        $now = now();
        $rows = [];
        foreach (self::TAXONOMY as [$slug, , , $aliases]) {
            $iid = $slugToId[$slug];
            foreach ($aliases as $alias) {
                $rows[] = [
                    'interest_id' => $iid,
                    'alias' => mb_substr(trim($alias), 0, 64),
                    'locale' => 'ru',
                    'created_at' => $now,
                    'updated_at' => $now,
                ];
            }
        }
        // insertOrIgnore — глобальный UNIQUE(lower(alias)) подстрахует от случайной коллизии меж узлами
        foreach (array_chunk($rows, 200) as $chunk) {
            DB::table('interest_aliases')->insertOrIgnore($chunk);
        }

        $this->command?->info(
            'Interests seeded: '.count(self::TAXONOMY).' узлов, '.count($rows).' алиасов.'
        );
    }
